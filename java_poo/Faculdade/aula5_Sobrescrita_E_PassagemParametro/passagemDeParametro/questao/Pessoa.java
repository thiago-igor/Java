package java_poo.Faculdade.aula5_Sobrescrita_E_PassagemParametro.passagemDeParametro.questao;

public class Pessoa {

    //ATRIBUTOS:
    private String nome;
    private int idade; 

    //CONSTRUTOR
    public Pessoa(String nome, int idade) {
        this.nome = nome;
        this.idade = idade;
    }

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public int getIdade() {
        return idade;
    }
    public void setIdade(int idade) {
        this.idade = idade;
    }

    
    
}
